package com.nicky.day1;

public class Demo8 {
    public static void main(String[] args) {

        //loop will increment from 1 and stop at 9
        for(int i=0;i<10;i++){
            System.out.println(i);
        }
        System.out.println();
        //loop will decrement from 10 and stop at 1
        for(int i =10;i>0;i--){
            System.out.println(i);
        }
    }
}
